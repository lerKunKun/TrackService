#!/bin/bash
# 简单的自动化部署脚本 (deploy.sh)

echo "=================================="
echo "      开始自动部署 Track-17 项目     "
echo "=================================="

# 1. 获取最新代码
echo "=> [1/3] 从 Git 仓库拉取最新代码..."
git pull

# 2. 重新构建并启动容器 (通过 -d 后台运行, --build 强制重新构建镜像)
echo "=> [2/3] 正在使用 docker-compose 重新构建并启动服务..."
docker-compose up -d --build

# 3. 清理废弃的无用镜像，释放服务器磁盘空间
echo "=> [3/3] 清理悬空(dangling)的 Docker 镜像..."
docker image prune -f

echo "=================================="
echo "          ✅ 部署成功完成!          "
echo "=================================="
